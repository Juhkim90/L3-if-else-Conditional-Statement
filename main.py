# Exercise: Write a conditional statement that evaluates positive / zero / negative numbers

number = int(input("Enter a number: "))

if (number > 0):
	print ("Your number is positive.")
if (number < 0):
	print ("Your number is negative.")
if (number == 0):
	print ("Your number is zero.")